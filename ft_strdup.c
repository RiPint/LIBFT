#include "libft.h"

char		*ft_strdup(const char *s)
{
	char	*dup;
	size_t	size;
	int		i;

	i = 0;
	size = ft_strlen(s);
	if (!(dup = (char *)malloc(sizeof(*dup) * (size + 1))))
		return (NULL);
	while (s[i] != '\0')
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
