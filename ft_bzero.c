#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char		*convert_s;
	size_t		i;

	i = 0;
	convert_s = (char *)s;
	while (i < n)
	{
		convert_s[i] = 0;
		i++;
	}
}
