#include <stdlib.h>
#include <string.h>
#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char		*d;
	unsigned char		*s;

	d = (unsigned char*)dst;
	s = (unsigned char*)src;
	if (dst < src)
		return (ft_memcpy(dst, src, len));
	while (len > 0)
	{
		d[len - 1] = s[len - 1];
		len--;
	}
	return (d);
}
