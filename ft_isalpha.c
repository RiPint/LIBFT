#include "libft.h"

int		ft_isalpha(int c)
{
	if (ft_isascii(c) == 1)
	{
		if (c >= 'a' && c <= 'z')
			return (1);
		else if (c >= 'A' && c <= 'Z')
			return (1);
		else
			return (0);
	}
	else
		return (0);
}
